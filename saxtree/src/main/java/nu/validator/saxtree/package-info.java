/**
 * This package provides SAX Tree: a tree model optimized for creation from SAX events and replay as SAX events.
 * 
 * <h2>Design Principles</h2>
 * <ol>
 * <li>Preserve information exposed through <code>ContentHandler</code>,
 *     <code>LexicalHandler</code> <em>and</em> <code>Locator</code>.
 * <li>Creation from SAX events or as part of the parse of a conforming
 *     HTML5 document should be <em>fast</em>.
 * <li>Emitting SAX events based on the tree should be <em>fast</em>.
 * <li>Mutations should be <em>possible</em> but should not make the above "fast" cases slower.
 * <li>Concurrent reads should work without locking when there are no concurrent mutations.
 * <li>The user of the API has the responsibility of using the API properly:
 *     for the sake of performance, the model does not check if it is being used properly.
 *     Improper use may, therefore, put the model in and inconsistent state.
 * </ol>
 */
